    function showTab(index) {
        const buttons = document.querySelectorAll('.tab-button');
        const textPanes = document.querySelectorAll('.col-md-4:nth-child(2) .tab-pane');
        const imagePanes = document.querySelectorAll('.col-md-4:nth-child(3) .tab-pane');

        buttons.forEach(btn => btn.classList.remove('active'));
        textPanes.forEach(p => p.classList.remove('active'));
        imagePanes.forEach(p => p.classList.remove('active'));

        buttons[index].classList.add('active');
        textPanes[index].classList.add('active');
        imagePanes[index].classList.add('active');
    }
    </script>



    <script>
    document.querySelectorAll('.tab-card').forEach(card => {
        card.addEventListener('click', () => {
            // Remove active-tab from all
            document.querySelectorAll('.tab-card').forEach(c => c.classList.remove(
                'active-tab'));
            card.classList.add('active-tab');

            // Hide all tab panes
            document.querySelectorAll('.tab-pane').forEach(pane => {
                pane.classList.remove('show', 'active');
            });

            // Show selected tab
            const targetId = card.getAttribute('data-target');
            const targetPane = document.getElementById(targetId);
            targetPane.classList.add('show', 'active');
        });
    });
    </script>





