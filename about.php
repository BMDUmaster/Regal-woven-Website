    <script>
    function animateCounter(counter, target) {
        let count = 0;
        const increment = Math.ceil(target / 100);
        counter.innerText = "0";

        const update = () => {
            if (count < target) {
                count += increment;
                counter.innerText = count;
                setTimeout(update, 20);
            } else {
                counter.innerText = target + "+";
            }
        };
        update();
    }

    document.querySelectorAll(".counter").forEach(counter => {
        const target = +counter.getAttribute("data-target");
        animateCounter(counter, target);
    });

    document.querySelectorAll(".hover-counter").forEach(box => {
        box.addEventListener("mouseenter", () => {
            const counter = box.querySelector(".counter");
            const target = +counter.getAttribute("data-target");
            animateCounter(counter, target);
        });
    });
    </script>

    <script>
    document.addEventListener("DOMContentLoaded", function() {
        const tabs = [{
                btnId: "what-tab",
                contentId: "content-what"
            },
            {
                btnId: "mission-tab",
                contentId: "content-mission"
            },
            {
                btnId: "why-tab",
                contentId: "content-why"
            }
        ];

        tabs.forEach(({
            btnId,
            contentId
        }) => {
            document.getElementById(btnId).addEventListener("click", function() {
                // Hide all content sections
                tabs.forEach(({
                    contentId
                }) => {
                    document.getElementById(contentId).style.display = "none";
                });

                // Show selected content
                document.getElementById(contentId).style.display = "block";

                // Optional: highlight current tab (if needed)
                tabs.forEach(({
                    btnId
                }) => {
                    document.getElementById(btnId).parentElement.classList
                        .remove(
                            "current");
                });
                this.parentElement.classList.add("current");
            });
        });
    });
    </script>


